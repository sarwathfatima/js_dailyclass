//Q:-FIND THE SUM OF 'n' NATURAL NUMBERS BY WHILE LOOP:
 let n = 1;
 let range=10;
//sum of no.
 let sum = 0;
 while(n <=range){
     sum = sum + n;
     n++
 }
  console.log(sum);//then the output will be "55"

